package com.example.learnkotlin

fun main() {
    println("welcome to kotlin")
    val a =45
    val b = 9

    // arithmetic operators
    println(a+b)
    println(a-b)
    println(a*b)
    println(a/b)

    // relational operators
    println(a>b)
    println(a<b)
    println(a<=b)
    println(a>=b)
    println(a==b)
    println(a!=b)

    // logical operators
    println(a==b||a>b)
    println(a!=b&& a>b)

}
